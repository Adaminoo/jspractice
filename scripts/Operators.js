//Arithmetic Operators

let a = 4;
let b = 3;

//Addition
let c = a + b; //c = 7

//Subtraction
let d = a - b; //d = 1

//Multiplication
let e = a * b; //e = 12

//Division
let f = a / b; //f = 1.33

//Modulus Division (Returns remainer)
let g = a % b; //g = 1

//Increment
let h = ++a; //h = 5

//Decrement
let i = --b //i = 2



